// Generate native/src/win_napi_shim.c — the self-contained Node-API binding shim
// for the cross-compiled Windows addon.
//
// Why this exists: on Windows the napi_* / node_api_* symbols are exported by the
// Electron host EXECUTABLE, not a shared library. The usual fix (an import lib +
// /delayload + a delay-load hook) is unavailable here — llvm-dlltool can't emit a
// delay import lib, and lld-link rejects `/delayload` from a `.drectve` directive
// (the only channel `zig build` can reach the linker through). So instead the shim
// DEFINES each napi symbol the addon references as a tiny naked `jmp` trampoline
// that forwards to a function pointer, and a DLL constructor fills those pointers
// at load time via GetProcAddress(GetModuleHandle(NULL), name) — i.e. against
// whatever host exe (electron.exe / the app exe / node.exe) loaded the addon. No
// import lib, no node.exe import, no linker directives: fully self-contained.
//
// The constructor runs during DLL_PROCESS_ATTACH, before the host calls
// napi_register_module_v1, so every pointer is live before the first napi call.
//
// Symbols are scraped from the vendored Node-API headers (native/include).
// Reproducible / CI-friendly: depends only on the headers (+ a C compiler at
// build time). Re-run whenever the headers change.
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const incDir = resolve(root, "native/include");
const outPath = resolve(root, "native/src/win_napi_shim.c");

// We EXPORT this from the addon (the napi entry point); it must not be shimmed.
const EXPORTED = new Set(["napi_register_module_v1"]);

const symbols = new Set();
for (const f of readdirSync(incDir)) {
  if (!f.endsWith(".h")) continue;
  const text = readFileSync(resolve(incDir, f), "utf8");
  for (const m of text.matchAll(/\b((?:napi|node_api)_[a-z0-9_]+)\s*\(/g)) {
    if (!EXPORTED.has(m[1])) symbols.add(m[1]);
  }
}

const names = [...symbols].sort();
if (names.length === 0) throw new Error("no napi symbols scraped from " + incDir);

const header = `// AUTO-GENERATED by scripts/gen-win-napi-shim.mjs — do not edit by hand.
//
// Windows-only Node-API binding shim. On Windows the napi_* symbols live in the
// Electron host executable, not a DLL, so each one is defined here as a naked
// jmp trampoline forwarding to a pointer resolved at DLL load via
// GetProcAddress(GetModuleHandle(NULL), name). See scripts/gen-win-napi-shim.mjs
// for the full rationale. Inert (#ifdef _WIN32) on every other OS.
#ifdef _WIN32
#include <windows.h>

// Each napi symbol: a forwarding pointer + a naked trampoline exporting the real
// name. main.zig's references bind to these trampolines; the trampoline jumps to
// the host's implementation through the pointer (RIP-relative, position-independent
// for the DLL). The pointers are filled by the constructor below before first use.
#define RIPTIDE_NAPI_SHIM(name) \\
  void *riptide_p_##name; \\
  __attribute__((naked)) void name(void) { __asm__ volatile("jmp *riptide_p_" #name "(%rip)"); }

`;

const shims = names.map((n) => `RIPTIDE_NAPI_SHIM(${n})`).join("\n");

const resolver = `

// Called by napi_register_module_v1 before the first napi trampoline can run.
// Binds every trampoline pointer to the host-exported implementation.
void riptide_resolve_napi(void) {
  HMODULE host = GetModuleHandleW(NULL);
#define RIPTIDE_NAPI_RESOLVE(name) riptide_p_##name = (void *)GetProcAddress(host, #name);
${names.map((n) => `  RIPTIDE_NAPI_RESOLVE(${n})`).join("\n")}
#undef RIPTIDE_NAPI_RESOLVE
}

#endif // _WIN32
`;

writeFileSync(outPath, header + shims + resolver);
console.log(`wrote ${outPath} (${names.length} napi trampolines)`);
